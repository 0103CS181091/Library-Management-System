    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(TF1.getText().trim().isEmpty() && PF1.getText().trim().isEmpty() && PF2.getText().trim().isEmpty()){
            lbl1.setText("Enter username.");
            lbl2.setText("Enter password.");
            lbl3.setText("Enter password.");
        }else if(TF1.getText().trim().isEmpty()){
            lbl1.setText("Enter password.");
        }else if(PF1.getText().trim().isEmpty()){
            lbl2.setText("Enter password.");
        }else if(PF2.getText().trim().isEmpty()){
            lbl3.setText("Enter password.");
        }else if(!PF1.getText().equals(PF2.getText())){
            lbl3.setText("Password does not match.");
        }else
        {

            String username=TF1.getText();
            String password=PF1.getText();
            Connection con=null;

            try
            {
                con=LMS.openConnection();
                PreparedStatement  ps=con.prepareStatement("insert into login values(?,?)");
                ps.setString(1,username);
                ps.setString(2, password);
                ps.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Signed up successfully.");
                    LoginForm h= new LoginForm();
                    h.setVisible(true);
                    h.pack();
                    h.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();

                

            }catch(Exception e)
            {
                System.out.println(e);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

