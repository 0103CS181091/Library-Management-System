    private void TF1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TF1KeyReleased
        // TODO add your handling code here:
        lbl1.setText("");
        String str=TF1.getText().toString();
        int index = 0;
        try {
            Connection con=null;
            con=LMS.openConnection();
            PreparedStatement PS=con.prepareStatement("select count(*) from login where username = '"+str+"'");

            ResultSet RS=PS.executeQuery();
            while(RS.next()){
                index=RS.getInt("count(*)");
                
            }
            if(index==1){
                lbl1.setText("username already exist.");
                jButton1.setEnabled(false);
            }else{
                jButton1.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_TF1KeyReleased

