    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(TF.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "you can not leave the text field empty. Please enter some data.");
        }
        else{
       jButton4.doClick();
        DefaultTableModel model=(DefaultTableModel) TB.getModel();
        String mainString;
        mainString = CB.getSelectedItem().toString();

        try {
            Connection con=null;
            con=LMS.openConnection();
            PreparedStatement PS=con.prepareStatement("select id,name,publisher,year,availability,genre,writer from bdetail where "+mainString+"='"+TF.getText()+"'");

            ResultSet RS=PS.executeQuery();
            while(RS.next()) {
                model.addRow(new Object[] {
                    RS.getString(1),RS.getString(2),RS.getString(3),RS.getString(4),RS.getString(5),RS.getString(6),RS.getString(7)
                });
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

